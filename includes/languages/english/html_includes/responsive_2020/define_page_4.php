<ul>
<li>You can submit your own artwork or graphics to us, and we’ll get your design print-ready, then show it to you for your approval.</li>
<li>Or, <a href="index.php?main_page=contact_us">contact us</a> to take advantage of our Artwork service: One of our professional designers can put together great-looking artwork based on your instructions and submit it to you for your approval. You can tell us your instructions when you order. Our artwork service is quick and efficient.</li>
</ul>
<p>Here's a list of guidelines to help you submit your artwork. If you have any questions, please <a href="index.php?main_page=contact_us">contact us</a>.</p>
<p><br></p>
<h4>Artwork</h4>
<ul style="padding-left:15px;">
  <li>
  For a quality print, we require no less than 300dpi.</li>
</ul>
  <img alt="Supply artwork at 300dpi for a crisp printed result" style="width:100%;max-width:840px;margin-bottom:10px;" src="images/artwork_res.jpg" title="Supply artwork at 300dpi for a crisp printed result">
<p style="padding-left:15px;">We take no responsibility for lesser quality due to low resolution files supplied.<br>
  </p>
<ul style="padding-left:15px;">
  <li>Artwork should be CMYK as RGB files will print discoloured.</li>
  <li>We require 2mm bleed all around. This means that anything touching the trim edge of your artwork requires bleed over the edge of your artwork by 2mm. We also recommend you keep a clean area of 2mm inside from the trim edge to be sure that nothing important will be lost during the print process.</li>
  <li style="margin-top:10px;">Label artwork with bleed</li>
</ul>
<img alt="Make sure all the important information in your artwork is within the safe zone" style="width:100%;max-width:840px;margin-bottom:20px;" src="images/artwork_bleed.gif" title="Make sure all the important information in your artwork is within the safe zone">
<ul style="padding-left:15px;">
  <li>Artwork that requires bleed needs to be checked that the label allows for bleed. Refer to die drawings for red &amp; green label 
  edges.</li>
  <li>All fonts must be outlined, and additional files embedded to retain the look of your artwork.</li>
  <li>Only supply artwork one up. (Do not layup your files.)</li>
  <li>The formats you can supply your artwork in are: PDF, EPS, TIF, JPEG, Illustrator, Photoshop, InDesign. (Anything else may include an additional charge, please contact us about this.)</li>
  <li>If your job requires a specific colour, please quote PMS colour. (We do our best to colour match however, there are 
  limitations depending on the print method.)</li>
  <li>Supply your file in it’s original/native format. (The program it was created in.)</li>
  <li>If your artwork is black, please use 100% black, and not four colour blacks.</li>
  <li>Please be aware compression settings may affect your print quality</li>
  <li>Flatten all transparency effects. If you don’t, although appearing fine on screen, there will be problems during the printing process.</li>
</ul>

